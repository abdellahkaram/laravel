     */
    public function store(Request $request)
    {
        //
        $risk = new Risk(); 
        $risk->name = $request->name; 
        $risk->desciption = $request->desciption; 
        $risk->state = $request->state; 
        $risk->project_id = $request->project_id; 
        $risk->save(); 
        return redirect('/project/'.$risk->project_id);
    }

